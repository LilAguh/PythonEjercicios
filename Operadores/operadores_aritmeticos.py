
#suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

print(suma)
print(resta)

#division y multiplicacion
multi = 12 * 5
division = 12 / 5 #la division siempre nos devuelve un float

print(multi)
print(division)

#potenciacion (exponente)
exponente = 12 ** 5

print(exponente)

#division baja
division_baja = 12 // 5

print(division_baja)

#resto o modulo
resto = 12 % 5

print(resto)


tipo_de_dato = type(division)
print(tipo_de_dato)