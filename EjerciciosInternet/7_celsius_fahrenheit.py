
# 6_ Escribir un programa que convierta grados Celsius a grados Fahrenheit.

print('-------------------------------------------------')

print('Ingrese los grados en Celsius para convertirlos a Fahrenheit:')
celsius = int(input(' '))

fahrenheit = (celsius * 1.8) + 32
    
print('-------------------------------------------------')

print(f'{celsius}°Celsius equivalen a {fahrenheit}°Fahrenheit')

print('-------------------------------------------------')