print('-------------------------------------------------')

print('Para calcular el area de un circulo nececitamos saber su radio en centimetros:')
numero = int(input(''))

area = 3.14 * (numero * numero)
    
print('-------------------------------------------------')

print(f'Si el radio del circulo es de {numero}cm,')
print(f'Su area es de {area}cm en total.')

